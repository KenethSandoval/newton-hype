module Numbers where

-- ==============================================
-- Redefinicion de map
-- map (2*) [1,2,3] -> [2,4,6]
-- ==============================================

n_map :: (a -> b) -> [a] -> [b]
n_map f xs = [f x | x <- xs]

-- ==============================================
-- Redefinicion de filter
-- filter even [1,3,5,4,2,6,1] -> [4,2,6]
-- filter (>3) [1,3,5,4,2,6,1] -> [5,4,6]
-- ==============================================

n_filter :: (a -> Bool) -> [a] -> [a]
n_filter  p xs = [x | x <- xs, p x]
